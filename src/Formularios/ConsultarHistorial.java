    private void consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarActionPerformed
        // TODO add your handling code here:
        listaOpciones.setViewportView(null);
        ServicioRespuesta respuesta=medico.obtenerHistorial(curp.getText());
        List<ConsultaMedicaBean> lista=(List<ConsultaMedicaBean>)respuesta.getResult();
        if(lista.size()==0){
            JOptionPane.showMessageDialog(null, "No se encontraron consultas registradas", "Busqueda de historial m√©dico",
                    JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            consultas=lista;
            insertarConsultas();
            this.curpS=curp.getText();
        }
    }//GEN-LAST:event_consultarActionPerformed
public void insertarConsultas(){
    JPanel panel=new JPanel();
    panel.setLayout(new GridLayout(consultas.size(),3));
    botones=new JButton[consultas.size()];
    //for(int J=0;J<3;J++)
    for(int i =0;i<consultas.size();i++){
        panel.add(new JLabel(""+(i+1)));
        panel.add(new JLabel(consultas.get(i).getFechaConsulta()));
        
        botones[i]=new JButton("Consultar");
        botones[i].addActionListener(this);
        botones[i].setBounds(new Rectangle(500,500,100,75));
        panel.add(botones[i]);
        
    }
    panel.setVisible(true);
    
    listaOpciones.setViewportView(panel);
    listaOpciones.updateUI();
}
@Override
    public void actionPerformed(ActionEvent ae) {
        for(int i=0;i<consultas.size();i++){
            if(ae.getSource()==botones[i]){
                medico.mostrarConsulta(this,consultas.get(i),curpS);
            }
        }
    }

