        JPanel panel = new JPanel();
        JTextField nombre = new JTextField();
        JTextField presentacion = new JTextField();
        JLabel name=new JLabel("Nombre: ");
        JLabel present=new JLabel("Presentaci√≥n: ");

        panel.setLayout(new GridLayout(2, 2));
        panel.add(name);
        panel.add(nombre);
