    private void BtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarActionPerformed
        nombre=jtf_nombre.getText();
        apeP=jtf_apep.getText();
        apeM=jtf_apem.getText();
        f_nac=dateNac.getText();//01/10/1999
        if (f_nac.length()<=9)
            f_nac="0"+f_nac;
        f_nac=f_nac.substring(6,10)+f_nac.substring(2,6)+f_nac.substring(0,2);
        tel=jtf_tel.getText();
        dir=jtf_dir.getText();
        sexo=comboSexo.getSelectedItem().toString();
        espe=comboEspe.getSelectedItem().toString();
        if (jtf_cedPro.getText().length()>0){
            cedula_prof=Integer.parseInt(jtf_cedPro.getText());
        }else{
            cedula_prof=0;
        }
        usuario=jtf_usu.getText();
        password=jtf_pass.getText();
        if (comboAdmin.getSelectedIndex()==0)
            admin="Usuario";
        else
            admin="Administrador";

        if (validarContenidoCampos()){
            MedicoAdministrador ma=new MedicoAdministrador();
            String estatus="";
            med_adm.cargarInfoMedico(nombre,apeP,apeM,f_nac,tel,dir,sexo,espe,cedula_prof,usuario,password,admin);
            estatus=med_adm.updateNuevoMedico();
            if (estatus.equals("OK")){
                this.dispose();
                JOptionPane.showMessageDialog(null, "El medico fue actualizado exitosamente.", "Actulizar Medico.", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "El medico NO fue actulizado exitosamente.\n\tERROR:"+estatus+".", "Actulizar Medico.", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ciertos campos no deben estar vacios, verifique.", "ALERTA",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BtnGuardarActionPerformed

