    private boolean contieneLetras(String cadena){
        boolean retorno=false;
        char a;
        for (int x=cadena.length();x>0;x--){
            a=cadena.charAt(x);
            if (Character.isLetter(a)){
                retorno=true;
                x=0;
            }
        }
        return retorno;
    }
