    public VerConsulta(JDialog parent, boolean modal, ConsultaMedicaBean consulta, String curp) {
        super(parent, modal);
        
        initComponents();
        capturador2.setPreferredSize(new java.awt.Dimension(247, 329));
        this.consultabean = consulta;        
        this.consulta = new ConsultaMedica(consulta);
        ServicioRespuesta respuesta = this.consulta.validarMedico(new MedicoBean(consultabean.getCedulaMedico()));
        if (respuesta.isSuccess()) {
            medico = (MedicoBean) respuesta.getResult();
            doctor.setText(medico.getNombre() + " " + medico.getApellidoPaterno() + " " + medico.getApellidoMaterno());
        } else {
            JOptionPane.showMessageDialog(null, "Medico No Existe:\n", "Medico No Encontrado", JOptionPane.ERROR_MESSAGE);
        }
        InsertarDatosPaciente(curp);
        ConstruirConsulta();
    }

    private void ConstruirConsulta() {
        try {
            fecha.setText(consultabean.getFechaConsulta());
            motivo.setText(consultabean.getMotivoConsulta());
            List<String> lista = consultabean.getManifestaciones();
            for (String s : lista) {
                modelo.addElement(s);
            }
            jList1.setModel(modelo);
            BufferedImage izq, der;
            izq = ImageIO.read(new File(consultabean.getOjoIzquierdo()));
            der = ImageIO.read(new File(consultabean.getOjoDerecho()));
            JLabel label = new JLabel(new ImageIcon(((new ImageIcon(izq)).getImage()).getScaledInstance(capturador2.getWidth(), capturador2.getHeight(), java.awt.Image.SCALE_SMOOTH)));
            label.setSize(capturador2.getWidth(), capturador2.getHeight());
            capturador2.add(label);
            capturador2.updateUI();
            JLabel label2 = new JLabel(new ImageIcon(((new ImageIcon(der)).getImage()).getScaledInstance(capturador1.getWidth(), capturador1.getHeight(), java.awt.Image.SCALE_SMOOTH)));
            label2.setSize(capturador1.getWidth(), capturador1.getHeight());
            capturador1.add(label2);
            capturador1.updateUI();
        } catch (IOException ex) {
            Logger.getLogger(VerConsulta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void InsertarDatosPaciente(String curp) {
        ServicioRespuesta respuesta = consulta.validarPaciente(curp);
        if (respuesta.isSuccess()) {
            Paciente p = (Paciente) respuesta.getResult();
            nombreP.setVisible(true);
            direcc.setVisible(true);
            sexo.setVisible(true);
            sangre.setVisible(true);
            fechanac.setVisible(true);
            nombreP.setText(p.getNombre() + " " + p.getApellidoPaterno() + " " + p.getApellidoMaterno());
            direcc.setText(p.getDireccion());
            sexo.setText(p.getSexo());
            sangre.setText(p.getTipoSangre());
            fechanac.setText(p.getFechaNacimiento());
            idPaciente.setText(curp);
        } else {
            JOptionPane.showMessageDialog(null, "Paciente No Existe:\n"
                    + "Por favor ingrese un Paciente valido", "Paciente No Encontrado",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

